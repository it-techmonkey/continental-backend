  // Get all available localities
  static List<String> getLocalities() {
    return propertyData.keys.toList()..sort();
  }

  // Get ROI data for a specific locality and property type
  static List<RoiDataPoint>? getROIData(String locality, String propertyType) {
    return propertyData[locality]?[propertyType];
  }

  // Get ROI data for a locality - uses first available property type
  static List<RoiDataPoint>? getROIDataForLocality(String locality) {
    final localityData = propertyData[locality];
    if (localityData == null || localityData.isEmpty) return null;
    // Return the data for the first available home type
    return localityData.values.first;
  }

  // Get current ROI (latest year)
  static double? getCurrentROI(String locality, String propertyType) {
    final data = getROIData(locality, propertyType);
    if (data == null || data.isEmpty) return null;
    return data.last.roi;
  }

  // Get current ROI for locality - uses first available property type
  static double? getCurrentROIForLocality(String locality) {
    final data = getROIDataForLocality(locality);
    if (data == null || data.isEmpty) return null;
    return data.last.roi;
  }
